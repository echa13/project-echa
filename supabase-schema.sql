-- 1. TABEL: services (Daftar Layanan Laundry)
CREATE TABLE IF NOT EXISTS public.services (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    service_name TEXT NOT NULL,
    price_per_kg NUMERIC NOT NULL,
    estimate_hours INT NOT NULL,
    is_available BOOLEAN DEFAULT true,
    icon TEXT DEFAULT '🧺',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. TABEL: orders (Data Transaksi Laundry)
CREATE TABLE IF NOT EXISTS public.orders (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT REFERENCES public.users(id) ON DELETE CASCADE,
    service_id BIGINT REFERENCES public.services(id) ON DELETE RESTRICT,
    weight_kg NUMERIC NOT NULL,
    total_price NUMERIC NOT NULL,
    status TEXT CHECK (status IN ('pending', 'proses', 'selesai', 'terkirim')) DEFAULT 'pending',
    pickup_address TEXT,
    notes TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

