-- 4. RLS POLICIES (Row Level Security)
-- Aktifkan RLS
ALTER TABLE public.services ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.orders ENABLE ROW LEVEL SECURITY;

-- Policy: services bisa dibaca semua user (termasuk guest via anon key)
CREATE POLICY "Services public read" ON public.services
    FOR SELECT USING (true);

-- Policy: orders - user hanya bisa melihat order milik sendiri
CREATE POLICY "Orders user read own" ON public.orders
    FOR SELECT USING (auth.uid() = user_id OR (SELECT role FROM public.users WHERE id = auth.uid()) = 'admin');

-- Policy: orders - user bisa insert order milik sendiri
CREATE POLICY "Orders user insert own" ON public.orders
    FOR INSERT WITH CHECK (auth.uid() = user_id);

-- 5. ENABLE REAL TIME (opsional)
ALTER PUBLICATION supabase_realtime ADD TABLE public.orders;
